function EmpForm({ emp, onChange, onSave, onCancel }) {
  const upd = (k, v) => onChange(e => ({ ...e, [k]: v }));
  return (
    <div>
      <div style={{display:"grid",gridTemplateColumns:"1fr 1fr",gap:10,marginBottom:14}}>
        {[["name","الاسم الكامل"],["dept","الإدارة"],["title","المسمى الوظيفي"],["city","المدينة"]].map(([k,l])=>(
          <div key={k}>
            <div style={{fontSize:11,color:"#94a3b8",marginBottom:4}}>{l}</div>
            {k==="title" ? (
              <select style={S.select} value={emp[k]||""} onChange={e=>upd(k,e.target.value)}>
                <option>موظف</option><option>رئيس قسم</option><option>مدير</option>
              </select>
            ) : (
              <input style={S.input} value={emp[k]||""} onChange={e=>upd(k,e.target.value)} />
            )}
          </div>
        ))}
        <div>
          <div style={{fontSize:11,color:"#94a3b8",marginBottom:4}}>الفجوة التدريبية (0-2)</div>
          <input style={S.input} type="number" min={0} max={2} step={0.1} value={emp.gap||0} onChange={e=>{
            const g=+e.target.value;
            upd("gap",g);
            upd("priority",g>=1.5?"عالٍ":g>=0.75?"متوسط":"منخفض");
          }} />
        </div>
        <div>
          <div style={{fontSize:11,color:"#94a3b8",marginBottom:4}}>الأولوية (تُحسب تلقائياً)</div>
          <input style={{...S.input,color:gc(emp.gap||0),fontWeight:700}} value={emp.priority||"منخفض"} readOnly />
        </div>
      </div>
      <div style={{fontSize:12,fontWeight:700,color:"#C8973A",marginBottom:8}}>الاحتياجات التدريبية</div>
      <div style={{display:"grid",gridTemplateColumns:"1fr 1fr",gap:8,marginBottom:16}}>
        {DOMAINS.map(d => (
          <div key={d} style={{display:"flex",alignItems:"center",justifyContent:"space-between",background:"#0f172a",borderRadius:8,padding:"7px 10px"}}>
            <span style={{fontSize:12,fontWeight:600}}>{d}</span>
            <select style={{...S.select,fontSize:11,padding:"3px 6px",width:90}} value={emp[d]||"-"} onChange={e=>upd(d,e.target.value)}>
              <option>-</option><option>منخفض</option><option>متوسط</option><option>عالٍ</option>
            </select>
          </div>
        ))}
      </div>
      <div style={{display:"flex",gap:10,justifyContent:"flex-end"}}>
        <button style={S.btn("#334155","#94a3b8")} onClick={onCancel}>إلغاء</button>
        <button style={S.btn("#217346")} onClick={() => onSave(emp)}>💾 حفظ</button>
      </div>
    </div>
  );
}
